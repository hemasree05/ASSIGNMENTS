def relational_operators(a, b):
    if a < b:
        print(f"{a} is less than {b}.")
    if a > b:
        print(f"{a} is greater than {b}.")
    if a >= b:
        print(f"{a} is greater than or equal to {b}.")
# Example usage
relational_operators(10, 20)  
relational_operators(20, 10)  
relational_operators(15, 15)  
