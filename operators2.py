def increment_decrement_operations(value):
    print("Initial value:", value)
    
    # Increment operation
    value += 1  # Equivalent to ++ in other languages
    print("After increment (value += 1):", value)
    
    # Decrement operation
    value -= 1  # Equivalent to -- in other languages
    print("After decrement (value -= 1):", value)

# Example
increment_decrement_operations(10)
